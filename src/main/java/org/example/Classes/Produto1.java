package org.example.Classes;

public class Produto1 {

    private String idProd;
    private String preco;
    private String tipoProd;
    private String nomeProd;
    private String pagamentoFormPag;
    private String estoqueIdProd;
    private String carrinhoIdCarrinho;


    public String getIdProd() {
        return idProd;
    }

    public void setIdProd(String idProd) {
        this.idProd = idProd;
    }

    public String getPreco() {
        return preco;
    }

    public void setPreco(String preco) {
        this.preco = preco;
    }

    public String getTipoProd() {
        return tipoProd;
    }

    public void setTipoProd(String tipoProd) {
        this.tipoProd = tipoProd;
    }

    public String getNomeProd() {
        return nomeProd;
    }

    public void setNomeProd(String nomeProd) {
        this.nomeProd = nomeProd;
    }

    public String getPagamentoFormPag() {
        return pagamentoFormPag;
    }

    public void setPagamentoFormPag(String pagamentoFormPag) {
        this.pagamentoFormPag = pagamentoFormPag;
    }

    public String getEstoqueIdProd() {
        return estoqueIdProd;
    }

    public void setEstoqueIdProd(String estoqueIdProd) {
        this.estoqueIdProd = estoqueIdProd;
    }

    public String getCarrinhoIdCarrinho() {
        return carrinhoIdCarrinho;
    }

    public void setCarrinhoIdCarrinho(String carrinhoIdCarrinho) {
        this.carrinhoIdCarrinho = carrinhoIdCarrinho;
    }
}
