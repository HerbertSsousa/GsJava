package org.example.Classes;

public class TrabVol {

    private String idEmpregado;
    private String areaInt;
    private String idade;
    private String cep;


    public String getIdEmpregado() {
        return idEmpregado;
    }

    public void setIdEmpregado(String idEmpregado) {
        this.idEmpregado = idEmpregado;
    }

    public String getAreaInt() {
        return areaInt;
    }

    public void setAreaInt(String areaInt) {
        this.areaInt = areaInt;
    }

    public String getIdade() {
        return idade;
    }

    public void setIdade(String idade) {
        this.idade = idade;
    }

    public String getCep() {
        return cep;
    }

    public void setCep(String cep) {
        this.cep = cep;
    }
}
